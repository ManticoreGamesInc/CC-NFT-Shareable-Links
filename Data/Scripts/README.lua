The NFT Shareable Links component makes copying URLs to NFTs a lot easier by using the Text Entry Box.
In the demo that comes with the component, a player can walk up to any of the photo frames and interact
with the trigger to open up the UI. The UI contains a text entry box that is auto-filled and selected
ready to be copied.

-----------------------------------------------------------------------------------------------------------------

====
NOTE
====

If the Dependent folders are empty in Project Content under Imported Content for this component, save and reload the project to fix it.

==========
How to use
==========

The component comes with 2 templates. A template that shows a demo of the component in use.
Also, a template is included to create your shareable links without all the other objects included.

Two custom properties need to be set for each shareable link.

- ContractAddress
  The contract address to look at when retrieving tokens.

- TokenID
  The token ID to load.

